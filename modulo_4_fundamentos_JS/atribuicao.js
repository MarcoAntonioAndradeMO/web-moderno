// "=" funçao de atribuição
const a = 7;
let b = 3;

// Soma de atribuição ==> "b = b + a"
b += a;
console.log(b);

// Subtração de atribuição ==> "b = b - 4"
b -= 4;
console.log(b)

// Multiplicação com atribuição ==> "b = b * 2"
b *= 2;
console.log(b)

// Divisão com atribuição ==> "b = b / 2"
b /= 2;
console.log(b)

// Modularização com atribuição ==> "b = b % 2"
b %= 2
console.log(b)